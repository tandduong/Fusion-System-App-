    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://triton.towson.edu:3360/?serverTimezone=EST#/tduong2db", "tduong2", "Cosc*rnm7");
            
            String sql = "select * from tduong2db.STAFF where STAFFID = ?";
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, id.getText());
            
            ResultSet rs = st.executeQuery();
            if(rs.next()){
                department.setText(rs.getString("DNUMS"));
                wage.setText(rs.getString("WAGE"));
                startDate.setText(rs.getString("DATESTARTED"));
                gradDate.setText(rs.getString("GRADDATE"));
                wh.setText(rs.getString("Worhours"));
                cpr.setText(rs.getString("CPR"));
            }else{
                JOptionPane.showMessageDialog(null, "Record not found");
            }
            con.close();
        }
        catch(Exception e){
            System.err.print(e);
        }
    }//GEN-LAST:event_searchActionPerformed

