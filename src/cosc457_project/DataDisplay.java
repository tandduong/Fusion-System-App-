    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://triton.towson.edu:3360/?serverTimezone=EST#/tduong2db", "tduong2", "Cosc*rnm7");
            
            String sql = "update tduong2db.STAFF set DNUMS = ?, WAGE =?, DATESTARTED =?, GRADDATE=?, Worhours = ?, CPR =? where STAFFID =?";
            
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, department.getText());
            st.setInt(2, Integer.parseInt(wage.getText()));
            st.setString(3, startDate.getText());
            st.setString(4, gradDate.getText());
            st.setString(5, wh.getText());
            st.setString(6, cpr.getText());
            st.setString(7, id.getText());
            
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Updated");
            con.close();
        }
        catch(Exception e){
            System.err.print(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

